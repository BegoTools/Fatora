i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: 'en',
    // إذا فُقد المفتاح في اللغة الحالية، ارجع إلى fallbackLng ('en') أولًا
    // قبل استدعاء parseMissingKeyHandler. هذا يمنع ظهور المفتاح الخام للمستخدم
    // طالما أن المفتاح موجود في لغة واحدة على الأقل.
    partialBundledLanguages: true,
    interpolation: {
      escapeValue: false,
    },
    detection: {
      order: ['localStorage', 'navigator'],
      caches: ['localStorage'],
    },
    // مفتاح ناقص بالكامل (مش موجود في أي لغة):
    // - في وضع التطوير: نُرجع علامة واضحة تساعد المطوّر على اكتشاف المشكلة
    // - في الإنتاج: نُرجع نصًا فارغًا بدلًا من المفتاح الخام حتى لا يراه المستخدم
    parseMissingKeyHandler: (key: string) => {
      if (import.meta.env.DEV) {
        console.warn(`⚠️ Missing translation key: ${key}`);
        return `⟨missing:${key}⟩`;
      }
      return '';
    },
    // تنبيه إضافي في الكونسول عند فقد المفتاح في اللغة الحالية فقط
    missingKeyHandler: (lngs, _ns, key, _fallbackValue) => {
      if (import.meta.env.DEV) {
        console.warn(`⚠️ i18n missing key "${key}" in [${lngs.join(',')}] — fallback used.`);
      }
    },
  });

export default i18n;
